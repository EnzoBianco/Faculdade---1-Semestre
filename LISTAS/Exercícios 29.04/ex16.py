Minha_Lista = ["Ana", "Carlos", "Beatriz", "Eduardo", "Sérgio"]

# Tamanho da lista (len). Mostre se o tamanho da lista é par ou ímpar.

Tamanho_da_Lista = len(Minha_Lista)
if Tamanho_da_Lista % 2 == 0:
    print(f"A Lista tem {Tamanho_da_Lista} elementos. Logo, possui um número par de elementos!")
else:
    print(f"A Lista tem {Tamanho_da_Lista} elementos. Logo, poussui um número ímpar de elementos!")